function findDisappearedNumbers(nums: number[]): number[] {
    const result: number[] = [];

    // Step 1: Mark visited numbers
    for (let i = 0; i < nums.length; i++) {
        const index = Math.abs(nums[i]) - 1;
        if (nums[index] > 0) {
            nums[index] = -nums[index];
        }
    }

    // Step 2: Collect missing numbers
    for (let i = 0; i < nums.length; i++) {
        if (nums[i] > 0) {
            result.push(i + 1);
        }
    }

    return result;
};